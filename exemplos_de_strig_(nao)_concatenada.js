
var ola = "30"  + 2
console.log(ola);
// aqui há a concatenação pois a string numeral mais o operador '+' faz com que haja concatenação.  

var oi = "valor" + 2
console.log(oi);

// aqui há a concatenação pois a string  mais o operador '+' faz com que haja concatenação.  

var oie = "20" * 2
console.log(oie);
// aqui não  há a concatenação pois a string numeral  mais o operador '*' faz com que haja a resolução da operação, da mesma forma se dá para os operadores '-' '/' '%'.
